        <div class="row" style="text-align:left">
            <div class="col-8 content">
                <span class="row body-title">
                    一、參加對象：
                </span>
                <ol class="row body-copy">
                    全國技專校院(含研究所)在學學生。
                </ol>
                <span class="row body-title">
                    二、競賽主題：
                </span>
                <ol class="row body-copy">
                    創意競賽宗旨為鼓勵動畫影視及電腦動畫相關之創意製作，創作方向在不違背善良風俗的原則上可以自由發展。
                </ol>
                <span class="row body-title">
                    三、組隊方式：    
                </span>
                <ol class="row body-copy">
                    由各大專校院相關科系學生自由組隊，每隊學生3人以下為原則，指導教師 1 人。實際完成人數超過上限亦可報名，但是獎勵出國名額以報名規定人數為上限(含老師最多四人)。
                </ol>
                <span class="row body-title">
                    四、競賽方式：
                </span>
                <ol class="row body-copy">
                    本次競賽共分「直接參賽組」、「培訓參賽組」及「一般參賽組」
                </ol>
                <ol class="row body-copy">
                    A 直接參賽組：     
                </ol>
                甄選技專校院（含研究所）應屆畢業生或在學生之完整動畫作品，不經培訓，直接輔導報名參加國際競賽與影展。
                B 培訓參賽組（限還需在校一年以上之技專院校學生）：

                報名需要繳交作品雛形以及創作說明書，從中選出優勝創作隊伍，提供成員進行暑期培育與訓練計畫，師資亦由國內專業人士、教授，以及國外知名動畫教師組成。(優勝同學需於7/10-7/26 至雲科大培訓3 週，並於隔年 4 月底前完成作品，否則視同放棄資格。)。
                C 一般參賽組(一般大學、非技職學校) ：

                開放一般大學、非技專院校（含研究所）應屆畢業生或在學生之完整動畫作品報名，優勝作品並不輔導出國參賽，僅頒發獎狀鼓勵創作。
                五、報名方式：
                一律採用線上報名。請先至活動網站 www.media.yuntech.edu.tw 註冊報名。

                將填完報名表轉存成電子檔案，再將作品存放在網路空間，最後將網路空間路徑與報名表電子檔案 email到本計畫工作小組電郵信箱 : a.festival2005@gmail.com
                六、獎勵辦法：
                A、直接參賽組

                優勝及佳作：作品將由主辦單位輔導送件參加各項國際影展，如能獲選進入國際競賽單元（International Competition）並具得獎資格者，將全額或部分(視當年度經費)補助優勝組師生出席國際競賽、影展，觀摩學習，優勝組師生將頒發教育部獎狀、佳作組師生頒發主辦單位獎狀。
                入選：頒發獎狀。
                B、培訓參賽組

                正取：提供三週培訓課程。培訓完成之作品需要參加隔年舉行的本競賽直接參賽組，並獲佳作以上成績後，才頒發主辦單位獎狀。培訓完成之作品輔導送件參加各項國際影展，若獲得國際影展通知入選競賽單元或得獎，將補助指導老師與創作學生出席國際競賽、影展，觀摩學習。
                備取若干名：如遇正取放棄資格，將依次遞補。
                C、一般參賽組：優勝及佳作以及入選，頒發主辦單位獎狀

                七、出國參展補助規定：(以下規定僅針對舉有出國參展資格之直接參賽組)
                每隊製作小組名單，以參賽時的報名表為主，報名截止後即不得要求變更。

                如獲入選國際競賽單元，每組補助報名表上實際參與製作同學三位以及指導老師一位，最多四個名額。
                受補助之學生，如正在服役中，因為申請時間漫長不及參展，將不予補助出國。

                每隊實際指導老師有兩名以上者，如獲國際獎項，僅補助報名資料的第一順位指導老師。如需替換指導師，需要原指導老師提出放棄聲明。

                全年度同一部影片如獲得2個以上國際影展競賽單元提名，只補助參加一個影展。

                如參賽者自行重複報名影展，導致影展取消該件報名資訊或影展聯絡窗口不同以致無法及時獲得影展通知訊息，承辦單位將取停止輔助參展。

                除了競賽辦法提供的國際知名影展之外，主辦單位依當年邀展情形，也會寄送作品至其他相關或學校影展參賽，若獲得獎項，則視當年經費狀況補助得獎隊伍參加鄰近地區的影展觀摩，作為獎勵。

                如因計畫前期得獎數多，而導致後段預算不足時，則僅提供部份補助或是取消出團。

                本年度計畫僅補助 舉辦時間在109年七月底前的影展。

                獲補助出團的學員需要依照規定返國兩周內撰寫、繳交參展心得報告以及參展照片，無法如期完成需要繳回補助款項。

                參展團隊將由承辦單位安排帶隊人員擔任領隊以及翻譯人員隨團參展。

                為避免影展參展活動變質成為其他行程，出團師生請勿安排家人隨團。參加影展行程中，將安排參加開、閉幕，影展期間每日皆要出席影展單元或活動。

                八、重要時程：
                A、直接參賽組:

                6/7 ：線上報名截止，並完成作品線上繳交。
                6/19：公佈優勝、佳作、入選名單
                B、培訓參賽組:

                6/7 ：線上報名截止，並完成作品線上繳交。
                6/19：公佈暑期培訓名單
                7/10~7/26暑期培訓營
                109年4/30完成作品繳交
                C、一般參賽組

                6/7 ：線上報名截止，並完成作品線上繳交。
                6/19：公佈優勝、佳作、入選名單
                九、作品繳交規格：
                A直接參賽組、C一般參賽組：
                採線上報名並以網路空間繳交作品(請參閱第五點報名方式)。作品規格如下：

                作品長度以10分鐘為限 HD1080規格(1920*1080、畫素比1.0、每秒24格)的QuickTime檔案。
                作品說明可以使用doc或pdf格式文件以A4大小完成，跟作品一起繳交即可。
                如果影片中有旁白或對話，請提供具有英文字幕的影片跟word或PDF檔案。如果沒有對話、旁白則不用。
                B培訓參賽組：
                採線上報名並以網路空間繳交作品(請參閱第五點報名方式)。作品規格如下：

                培訓作品長度以10分鐘為限， HD1080(同上)或HD720規格(1024*768、畫素比1.0、每秒24格)的QuickTime檔案。
                上述作品如僅在前置作業，可以繳交闡述創作理念之動態腳本(animatic)或Story Board、前製模型、圖稿等，一併以網路繳交。
                作品說明：請以doc或pdf格式文件以A4大小完成，同上述作品在網路空間繳交即可。
                其他可以佐證創作潛力的作品(2D、3D動畫、手繪、靜態作品皆可)或獎狀、文件等，可以掃描後電子檔案上述作品在網路空間繳交。
                十、評審標準：
                創意(40%)、媒體運用技巧(40%)、風格與競賽潛力(20%)
                十一、國外影展輔導作品資料：
                美國ACM SIGGRAPH電腦動畫展 ACM SIGGRAPH Computer Animation Festival
                法國安錫國際動畫影展 Annecy International Animated Film Festival
                德國斯圖加特動畫影展 Stuttgart Festival of Animated Film
                日本東京動畫大賞 Tokyo Anime Award Festival
                奧地利Tricky Women 國際動畫影展 Austria Tricky Women International Animation Festival
                韓國首爾卡通及動畫影展 Seoul International Cartoon & Animation Festival
                韓國富川PISAF國際動畫影展 Puchon International Animation Festival
                澳洲墨爾本國際動畫影展 Melbourne International Animation Festival (MIAF)
                保加利亞Golden Kuker Sofia國際動畫影展 Golden Kuker Sofia-International Animation Film Festival
                羅馬尼亞Kinofest國際數位影展 KINOFEST International Digital Film Festival
                其他小型影展報名資料請參閱報名網站的最新消息。
                十二、其他規定：
                本項辦法，若有變動或不足之處，依計畫網站更新資料為準。www.media.yuntech.edu.tw
